#include "libft.h"

char	*ft_strcat(char *restrict s1, char *restrict s2)
{
	int i;
	int m;

	m = 0;
	i = 0;
	while (s1[i])
	{
		i++;
	}
	while (s2[m])
	{
		s1[i] = s2[m];
		m++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
