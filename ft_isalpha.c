#include "libft.h"

int		ft_isalpha(int m)
{
	unsigned char m1;

	m1 = (unsigned char)m;
	if ((m >= 'A' && m <= 'Z') || (m >= 'a' && m <= 'z'))
		return (1);
	else
		return (0);
}
